const CACHE_NAME = 'asnet-cache-v1';
const urlsToCache = [
  '/',
  '/css/asnet-theme.css',
  '/img/branding/logo-asnet.png'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        if (response) {
          return response;
        }
        return fetch(event.request);
      })
  );
});
